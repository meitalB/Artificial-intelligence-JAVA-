public class point {
    //the class present a point class
    private double x;
    private double y;
    //constractor
     public point(double x,double y){
         this.x=x;
         this.y=y;
     }
     //get
     public double x(){

         return this.x;
     }
     //get
     public double y(){
         return this.y;
     }
}
